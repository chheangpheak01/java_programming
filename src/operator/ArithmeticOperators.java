package operator;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 2, b = 5;
        int sum = a + b;
        int sub = a - b;
        int multi= a * b;
        float div = a / b;
        int mudulo = a % b;
        System.out.println(sum);
        System.out.println(sub);
        System.out.println(multi);
        System.out.println(div);
        System.out.println(mudulo);
    }
}
